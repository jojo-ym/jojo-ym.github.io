var total = 0;

function loadCategories() {
    var categoriesContainer = document.getElementById("categories");

    products.forEach((product, index) => {
        categoriesContainer.innerHTML += `
          <div onclick="loadProducts('`+ index + `')" class="card shadow-sm mx-1 custom-button-categ text-center d-flex justify-content-center" style="cursor: pointer; padding: 2px; height: 50px; width: 150px; background-color: rgb(249, 246, 246); color: rgb(8,8,8)">
            <small class="categoryName" style="font-weight: 400;">`+ product.category + `</small>
          </div>
        `;
    });
}



function loadProducts(categoryIndex) {
    const productContainer = document.getElementById("productContainer");
    productContainer.innerHTML = "";
    products[categoryIndex].contents.forEach((content) => {
        productContainer.innerHTML +=
            `  <div onclick="addToReceipt('` + content.price + `','` + content.code + `')"
                                        class="card shadow mx-1 my-2 custom-button content p-3"
                                        style="height: 45vh; background-color: white;">
                                        <img src="image/`+ content.image + `.avif" alt="" width="100%">
                                        <small class="contentName" style="font-weight: 500; padding-top: 7px; color: rgb(8,8,8)">`+ content.name + `</small>
                                        <small class="contentPrice" style="color: rgb(8,8,8)">PHP `+ content.price + `</small>
                </div>
     `;
    });
}


function addToReceipt(price, code) {
    var receiptContainer = document.getElementById("receipt");
    total = parseFloat(total) + parseFloat(price);

    totalValueElement = document.getElementById("totalValue");
    totalValueElement.innerHTML = `PHP `+total;

    receiptContainer.innerHTML += `
            <div class="d-flex flex-row justify-content-between px-4">
                <div class="small receiptCode" style="color: rgb(8,8,8)">`+ code + `</div>
                <div class="small receiptPrice" style="color: rgb(8,8,8)">PHP `+ price + `</div>  
            </div>      
      `;
}

